<script>
import { mapGetters } from "vuex";

export default {
  middleware: "auth",

  data() {
    return {
      showLogoutModal: false,
      showDropdown: false,
    };
  },
  computed: {
    ...mapGetters({
      userProfile: "auth/getUserProfile",
    }),
    pageTitle() {
      let title = "Profile";
      if (this.$route.fullPath == "/profile/address") {
        title = "Address";
      } else if (this.$route.fullPath == "/profile/orders") {
        title = "Orders";
      } else if (this.$route.fullPath == "/profile/promotions") {
        // Added title for Promotions
        title = "Promotions";
      } else if (this.$route.fullPath == "/profile/payments") {
        // Added title for Payments
        title = "Payments";
      } else if (this.$route.fullPath == "/profile/favorite-products") {
        // Added title for favorite-products
        title = "Favorite products";
      } else if (this.$route.fullPath == "/profile/settings") {
        // Added title for Payments
        title = "Settings";
      }

      return title;
    },
  },
  methods: {
    handleLogout() {
      this.showLogoutModal = false;
      this.$cookies.remove("token");
      this.$router.push("/auth/login");
    },
    dropdownToggle() {
      this.showDropdown = !this.showDropdown;
    },
  },
};
</script>

